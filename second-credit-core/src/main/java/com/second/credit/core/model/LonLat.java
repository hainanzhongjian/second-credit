package com.second.credit.core.model;

/**
 * @note 经纬度实体
 * @author wangmeng
 * @date 2015年8月9日下午3:46:18
 */
public class LonLat {

    private double lon; // 经度
    private double lat; // 维度

    public double getLon() {
        return lon;
    }

    public void setLon(double lon) {
        this.lon = lon;
    }

    public double getLat() {
        return lat;
    }

    public void setLat(double lat) {
        this.lat = lat;
    }

}
